import ClientAPI from 'ClientAPI'

export default class DatabaseAPI extends ClientAPI {

  /////////////////////////////////////////////////////////
  // Class constructor
  //
  /////////////////////////////////////////////////////////
  constructor (apiUrl) {

    super (apiUrl)
  }

  /////////////////////////////////////////////////////////////////
  //
  //
  /////////////////////////////////////////////////////////////////
  getData (dbName, assetID) {

    const url = dbName + "/" + assetID

    return this.ajax(url)
  }

  
}
